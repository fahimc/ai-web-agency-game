import { readdir, readFile, stat, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const vendorRoot = join(root, 'template-library', 'vendor');
const libraryPath = join(root, 'template-library', 'templates.json');
const dataPath = join(root, 'src', 'data', 'templateLibrary.js');

const templateDirs = existsSync(vendorRoot)
  ? (await readdir(vendorRoot)).sort()
  : [];

const templates = [];

for (const dir of templateDirs) {
  const folder = join(vendorRoot, dir);
  const folderStat = await stat(folder);
  if (!folderStat.isDirectory()) continue;

  const sourcePath = join(folder, '.microagency-source.json');
  if (!existsSync(sourcePath)) continue;

  const source = JSON.parse(await readFile(sourcePath, 'utf8'));
  const files = await collectFiles(folder);
  const htmlFiles = files.filter((file) => file.endsWith('.html')).slice(0, 12);
  const cssFiles = files.filter((file) => file.endsWith('.css')).slice(0, 8);
  const jsFiles = files.filter((file) => file.endsWith('.js')).slice(0, 8);
  const sampleText = await readSampleText(folder, htmlFiles, cssFiles, jsFiles);
  const lower = sampleText.toLowerCase();
  const packageJson = await readJsonIfExists(join(folder, 'package.json'));
  const readme = await readTextIfExists(join(folder, 'README.md'));

  templates.push({
    id: source.id,
    name: source.name,
    sourceUrl: source.url,
    license: source.verifiedLicense || source.license || 'MIT',
    framework: source.framework || packageJson?.dependencies?.bootstrap ? 'Bootstrap' : 'Bootstrap',
    useCases: source.useCases || inferUseCases(source, lower),
    priority: source.priority || 'medium',
    description: describeTemplate(source, lower, readme),
    htmlFiles,
    cssFiles,
    jsFiles,
    hasImages: files.some((file) => /\.(jpg|jpeg|png|webp|gif|svg)$/i.test(file)),
    hasNavbar: lower.includes('navbar') || lower.includes('<nav'),
    hasCarousel: lower.includes('carousel'),
    hasModal: lower.includes('modal'),
    hasPortfolioGrid: lower.includes('portfolio') || lower.includes('project'),
    hasTimeline: lower.includes('timeline'),
    hasContactForm: lower.includes('<form') || lower.includes('contact'),
    hasPricing: lower.includes('pricing') || lower.includes('price'),
    hasBlog: lower.includes('blog') || lower.includes('post'),
    hasShop: lower.includes('shop') || lower.includes('product') || lower.includes('cart'),
    hasParallax: lower.includes('parallax') || lower.includes('background-attachment'),
    hasScrollAnimation: lower.includes('scroll') || lower.includes('aos') || lower.includes('wow'),
    hasVideo: lower.includes('<video') || lower.includes('youtube') || lower.includes('vimeo'),
    bootstrapComponents: inferBootstrapComponents(lower),
    sectionPatterns: inferSectionPatterns(source, lower),
    motionPatterns: inferMotionPatterns(lower),
    llmGuidance: guidanceFor(source, lower),
  });
}

await writeFile(libraryPath, JSON.stringify({
  version: 1,
  generatedAt: new Date().toISOString(),
  count: templates.length,
  licensePolicy: 'Only license-verified templates are included. Treat them as pattern references for MicroAgency section output.',
  templates,
}, null, 2));

await writeFile(dataPath, `// Generated by tools/generate-template-knowledge.mjs.
// Keep this compact: it is fed into the LLM as design/development context.
export const downloadedTemplateLibrary = ${JSON.stringify(templates, null, 2)};

export function downloadedTemplateSummary(limit = 80) {
  return downloadedTemplateLibrary
    .slice(0, limit)
    .map((template) => {
      const useCases = (template.useCases || []).join(', ');
      const sections = (template.sectionPatterns || []).join(', ');
      const motion = (template.motionPatterns || []).join(', ') || 'none detected';
      return \`\${template.id}: \${template.name}. Use cases: \${useCases}. Sections: \${sections}. Motion: \${motion}. Guidance: \${template.llmGuidance}\`;
    })
    .join('\\n');
}
`);

console.log(`Generated ${templates.length} template knowledge entries.`);

async function collectFiles(folder, prefix = '') {
  const entries = await readdir(join(folder, prefix), { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    if (entry.name === '.git' || entry.name === 'node_modules' || entry.name === '.next' || entry.name === 'dist') {
      if (entry.name === 'dist') {
        files.push(...await collectFiles(folder, join(prefix, entry.name)));
      }
      continue;
    }
    const relative = join(prefix, entry.name).replace(/\\/g, '/');
    if (entry.isDirectory()) {
      files.push(...await collectFiles(folder, relative));
    } else {
      files.push(relative);
    }
  }
  return files;
}

async function readSampleText(folder, htmlFiles, cssFiles, jsFiles) {
  const candidates = [...htmlFiles.slice(0, 5), ...cssFiles.slice(0, 3), ...jsFiles.slice(0, 3)];
  const parts = [];
  for (const file of candidates) {
    const text = await readTextIfExists(join(folder, file));
    if (text) parts.push(text.slice(0, 24000));
  }
  return parts.join('\n');
}

async function readTextIfExists(path) {
  if (!existsSync(path)) return '';
  return readFile(path, 'utf8').catch(() => '');
}

async function readJsonIfExists(path) {
  const text = await readTextIfExists(path);
  if (!text) return null;
  try {
    return JSON.parse(text);
  } catch {
    return null;
  }
}

function inferUseCases(source, lower) {
  const cases = new Set(source.useCases || []);
  if (lower.includes('portfolio')) cases.add('portfolio');
  if (lower.includes('blog')) cases.add('blog');
  if (lower.includes('shop') || lower.includes('product')) cases.add('commerce');
  if (lower.includes('agency')) cases.add('agency');
  if (lower.includes('contact')) cases.add('lead generation');
  return [...cases];
}

function describeTemplate(source, lower, readme) {
  const base = `${source.name} is a ${source.framework || 'Bootstrap'} template for ${(source.useCases || ['business websites']).join(', ')}.`;
  const extras = [];
  if (lower.includes('portfolio')) extras.push('portfolio presentation');
  if (lower.includes('timeline')) extras.push('timeline storytelling');
  if (lower.includes('pricing')) extras.push('pricing comparison');
  if (lower.includes('blog')) extras.push('content publishing');
  if (lower.includes('carousel')) extras.push('carousel-led proof or media');
  const readmeHint = readme.match(/Start Bootstrap - ([^\n]+)/i)?.[1];
  return [base, extras.length ? `Useful patterns: ${extras.join(', ')}.` : '', readmeHint ? `Readme title: ${readmeHint}.` : ''].filter(Boolean).join(' ');
}

function inferBootstrapComponents(lower) {
  const components = [];
  [
    'navbar', 'dropdown', 'collapse', 'offcanvas', 'accordion', 'modal',
    'carousel', 'card', 'btn', 'badge', 'breadcrumb', 'pagination',
    'list-group', 'form', 'table',
  ].forEach((name) => {
    if (lower.includes(name)) components.push(name);
  });
  return components;
}

function inferSectionPatterns(source, lower) {
  const patterns = new Set();
  if (lower.includes('navbar') || lower.includes('<nav')) patterns.add('responsive header');
  if (lower.includes('masthead') || lower.includes('hero')) patterns.add('hero');
  if (lower.includes('service')) patterns.add('services');
  if (lower.includes('feature')) patterns.add('features');
  if (lower.includes('portfolio') || lower.includes('project')) patterns.add('portfolio');
  if (lower.includes('timeline') || lower.includes('process')) patterns.add('process/timeline');
  if (lower.includes('testimonial') || lower.includes('review')) patterns.add('testimonials');
  if (lower.includes('pricing') || lower.includes('price')) patterns.add('pricing');
  if (lower.includes('blog') || lower.includes('post')) patterns.add('blog/articles');
  if (lower.includes('gallery')) patterns.add('gallery');
  if (lower.includes('contact') || lower.includes('<form')) patterns.add('contact/lead form');
  if (lower.includes('footer')) patterns.add('footer');
  if (!patterns.size && source.useCases) source.useCases.forEach((item) => patterns.add(item));
  return [...patterns];
}

function inferMotionPatterns(lower) {
  const patterns = [];
  if (lower.includes('parallax') || lower.includes('background-attachment')) patterns.push('parallax background/media');
  if (lower.includes('scroll')) patterns.push('scroll navigation/reveal');
  if (lower.includes('carousel')) patterns.push('carousel motion');
  if (lower.includes('collapse') || lower.includes('accordion')) patterns.push('expand/collapse interaction');
  if (lower.includes('modal')) patterns.push('modal overlays');
  return patterns;
}

function guidanceFor(source, lower) {
  const guidance = [];
  if (lower.includes('portfolio')) guidance.push('use for strong visual case-study or gallery sections');
  if (lower.includes('timeline')) guidance.push('adapt timeline into process or company story');
  if (lower.includes('blog')) guidance.push('use article/listing structure for insights or case studies');
  if (lower.includes('shop') || lower.includes('product')) guidance.push('use product grid/detail patterns for catalogues and service menus');
  if (lower.includes('carousel')) guidance.push('use carousels sparingly for testimonials or featured work');
  if (!guidance.length) guidance.push(`use as a ${source.useCases?.[0] || 'business website'} composition reference`);
  return guidance.join('; ') + '. Rebuild with MicroAgency sections, validated theme tokens, accessible Bootstrap markup, and original client-specific copy.';
}
